         'Tiles','Tiles','Tiles','Tiles','Tiles','Tiles','Tiles','Tiles','Tiles','Tiles','Tiles','Tiles','Tiles', ...
         'Clovers','Clovers','Clovers','Clovers','Clovers','Clovers','Clovers','Clovers','Clovers','Clovers','Clovers','Clovers','Clovers', ...
         'Pikes','Pikes','Pikes','Pikes','Pikes','Pikes','Pikes','Pikes','Pikes','Pikes','Pikes','Pikes','Pikes', ...
         'Joker','Joker','Joker'};

Card3j_index=randperm(55);%makes random shuffled index list of cards

for n=1:length(Card3j_index)
    cards3j(n).name=card3j_name(Card3j_index(n),:);
    cards3j(n).suit=suits{Card3j_index(n)};
    cards3j(n).rank=ranks{Card3j_index(n)};
    cards3j(n).value=card3j_values(Card3j_index(n));
end